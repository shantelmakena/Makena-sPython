# A simple calculator program

num1 = int(input("Enter first number"))
num2 = int(input("Enter second number"))


# Addition
print("{} + {}".format(num1,num2))
print(num1 + num2)

# Subtraction
print("{} - {}".format(num1,num2))
print(num1 - num2)



# Division
print("{} / {}".format(num1,num2))
print(num1 / num2)



# Multiplication
print("{} * {}".format(num1,num2))
print(num1 * num2)